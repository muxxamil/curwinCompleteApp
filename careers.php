		$("#apply-job-form").bind('submit',(function(e) {
		    e.preventDefault();
		    $.ajax({
		        url: 'php/applyForJob.php',
		        type: "POST",
		        data:  new FormData(this),
		        contentType: false,
		        cache: false,
		        processData: false,
		        success: function(data) {
		        	console.log("data", data);
		        	console.log("data.status", data.status);
		        	// data = JSON.parse(data);
		            if(data && data.status == 400) {
		            	$("#applyJobErr").show();
		            	$("#applyJobSuccess").hide();
		            	$("#applyJobWrong").hide();
		            } else if(data && data.status == 200) {
		            	$("#applyJobSuccess").show();
		            	$("#applyJobErr").hide();
		            	$("#applyJobWrong").hide();
		            } else {
		            	$("#applyJobWrong").show();
		            	$("#applyJobSuccess").hide();
		            	$("#applyJobErr").hide();
		            }
		        }
		    });
		}));

