		function applyJobModal(identifier) {
			var jobTitle = $(identifier).data('jobtitle');
		     $('#applyForJobModalLabel').html('Apply for ' + jobTitle);
		     $("#jobSubject").val( jobTitle );
		     // As pointed out in comments, 
